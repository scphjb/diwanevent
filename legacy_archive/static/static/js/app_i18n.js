function translateDOM(lang) {
    if (lang === 'ar') {
        document.documentElement.dir = 'rtl';
        // Check if we were in another language and need to reload to restore original Arabic
        if (window.originalLang && window.originalLang !== 'ar') {
            location.reload();
        }
        return;
    }

    document.documentElement.dir = 'ltr';
    const dict = adminTranslations[lang];
    if (!dict) return;

    // Save original state reference
    if (!window.originalLang) window.originalLang = 'ar';

    // Fast translation mapping using TreeWalker for text nodes
    const walk = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, null, false);
    let node;
    while (node = walk.nextNode()) {
        let text = node.nodeValue.trim();
        if (text && dict[text]) {
            node.nodeValue = node.nodeValue.replace(text, dict[text]);
        } else if (text) {
            // Partial match for combo strings
            for (let arText in dict) {
                if (text.includes(arText) && arText.length > 3) {
                    node.nodeValue = node.nodeValue.replace(arText, dict[arText]);
                    text = node.nodeValue.trim();
                }
            }
        }
    }

    // Translate placeholders
    document.querySelectorAll('input, textarea').forEach(el => {
        const ph = el.getAttribute('placeholder');
        if (ph && dict[ph.trim()]) {
            el.setAttribute('placeholder', dict[ph.trim()]);
        }
    });

    // Translate specific data-i18n attributes
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (dict[key]) {
            // If it's a span/button, update text; if it's an input/textarea, it might have placeholder
            if (el.tagName === 'SPAN' || el.tagName === 'BUTTON' || el.tagName === 'DIV' || el.tagName === 'LABEL' || el.tagName === 'TH' || el.tagName === 'TD') {
                el.innerHTML = el.innerHTML.replace(key, dict[key]);
            } else {
                el.innerHTML = dict[key];
            }
        }
    });

    // Translate placeholders from data-i18n-placeholder
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (dict[key]) {
            el.setAttribute('placeholder', dict[key]);
        }
    });

    // Translate value attributes for buttons if they are literal Arabic
    document.querySelectorAll('input[type="button"], input[type="submit"]').forEach(el => {
        const val = el.value.trim();
        if (val && dict[val]) {
            el.value = dict[val];
        }
    });

    window.originalLang = lang;
}

document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('diwan_admin_lang') || 'ar';
    if (savedLang !== 'ar') {
        // slight delay to let dynamic elements render
        setTimeout(() => translateDOM(savedLang), 150);
    }
});

function switchAdminLang(lang) {
    localStorage.setItem('diwan_admin_lang', lang);
    if (lang === 'ar') {
        location.reload();
    } else {
        translateDOM(lang);
    }
}
