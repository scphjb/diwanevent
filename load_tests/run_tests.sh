#!/usr/bin/env bash
# =====================================================================
# سكريبت التشغيل التلقائي لاختبارات الأداء (Locust Headless Engine)
# =====================================================================

# إيقاف التنفيذ عند حدوث أي خطأ
set -e

# ألوان المخرجات للتنبيه
GREEN='\033[0;32m'
RED='\033[0;31m'
YELLOW='\033[1;33m'
NC='\033[0m' # No Color

echo -e "${GREEN}🚀 البدء في تشغيل جناح اختبارات أداء منصة ديوان (Diwan Load Test)...${NC}\n"

# 1. تهيئة بيئة الاختبار وإعداد البيانات
echo -e "${YELLOW}[1/3] تهيئة بيانات الفعالية والمشتركين في قاعدة البيانات...${NC}"
python conftest.py

# 2. إنشاء مجلد التقارير
mkdir -p reports

# 3. تشغيل اختبار الأداء الرئيسي (Headless Mode)
# محاكاة 1200 مستخدم متزامن، مع إضافة 50 مستخدم جديد في الثانية
echo -e "\n${YELLOW}[2/3] تشغيل اختبار الأداء الرئيسي (1200 مستخدم متزامن)...${NC}"
echo -e "سيبدأ الاختبار الآن بشكل صامت (Headless) لمدة 3 دقائق..."

locust -f locustfile.py \
  --headless \
  -u 1200 \
  -r 50 \
  --run-time 3m \
  --csv=reports/diwan_perf_report \
  --html=reports/diwan_perf_report.html \
  --host=http://localhost:8000

echo -e "\n${GREEN}✅ انتهى اختبار الأداء الرئيسي بنجاح!${NC}"
echo -e "📄 تم حفظ التقارير في مجلد: ${GREEN}load_tests/reports/${NC}"

# 4. تقييم النتائج مقابل معايير الجودة (Thresholds)
echo -e "\n${YELLOW}[3/3] تقييم الأداء الفني مقارنة بمعايير القبول والرفض...${NC}"

# قراءة مخرجات CSV وحساب الـ percentiles الأساسية لعرضها في الشاشة
if [ -f "reports/diwan_perf_report_stats.csv" ]; then
  echo -e "\n📊 ملخص إحصائيات الأداء المستخرجة:"
  echo -e "--------------------------------------------------------"
  column -t -s, reports/diwan_perf_report_stats.csv | head -n 10
  echo -e "--------------------------------------------------------"
else
  echo -e "${RED}⚠️ لم يتم العثور على ملف الإحصائيات CSV.${NC}"
fi

echo -e "\n${GREEN}🎉 اكتمل تشغيل جناح اختبار الأداء بالكامل بنجاح!${NC}"
